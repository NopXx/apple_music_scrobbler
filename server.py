class Tracker:
    MAX_MISS_BEFORE_CLEAR = 3  # ทน osascript พลาดได้ 3 รอบ (~6 วิ) ก่อนจะล้าง UI
    STOPPED_CONFIRM_COUNT = 2  # ต้องเห็น stopped ติดกันสองครั้งก่อนจะเชื่อว่าหยุดจริง

    def __init__(self):
        self.last_track_id = ""
        self.last_position = 0.0
        self.last_state = "stopped"
        self.track_start_ts = 0
        self.has_scrobbled = False
        # track_id ล่าสุดที่ scrobble ไปแล้ว — ใช้กันไม่ให้ scrobble ซ้ำเมื่อ pause/play เพลงเดิม
        # จะถูกล้างเมื่อ replay (เริ่มใหม่จากต้น) หรือเมื่อ Music หยุดจริง
        self.last_scrobbled_track_id = ""
        self.current_artwork_url = None
        self.current_media = {}
        # เก็บชื่อทั้งก่อน/หลัง apply edits — ใช้ตอน reload/edit จาก UI
        self.current_original = {"artist": "", "track": "", "album": ""}
        self.current_edited = {"artist": "", "track": "", "album": ""}
        self.stop_event = threading.Event()
        # debounce state
        self.last_good_payload = None
        self.miss_count = 0
        self.stopped_count = 0

    def run(self):
        while not self.stop_event.is_set():
            try:
                self.tick()
            except Exception as e:
                print(f"[tracker] error: {e}")
            self.stop_event.wait(2.0)

    def stop(self):
        self.stop_event.set()

    def tick(self):
        # Heartbeat log for debugging bundle issues
        print(".", end="", flush=True) 
        
        info = query_music()

        # osascript fail / parse ไม่ได้ → ค้างสถานะเดิมไว้ก่อน
        if info is None:
            self.miss_count += 1
            if self.miss_count >= self.MAX_MISS_BEFORE_CLEAR:
                self.last_good_payload = None
                self._write_now_playing({"playing": False})
            # ถ้ายังไม่เกิน threshold ไม่เขียนอะไรใหม่ — คง snapshot เดิม
            return

        # Music หยุดจริง ๆ — ต้องเห็นติดกันถึงจะเชื่อ
        if info == "stopped":
            self.miss_count = 0
            self.stopped_count += 1
            if self.stopped_count >= self.STOPPED_CONFIRM_COUNT:
                # ถ้าเคยเล่นอยู่ ให้ยิง pause ไปบอก webhook ด้วย
                if self.last_state == "playing" and self.last_good_payload:
                    self._fire("stopped", self.last_good_payload)

                self.last_good_payload = None
                self.last_track_id = ""
                self.last_scrobbled_track_id = ""
                self.last_state = "stopped"
                self._write_now_playing({"playing": False})
            return

        # รอบนี้อ่านได้สมบูรณ์ — reset counter
        self.miss_count = 0
        self.stopped_count = 0

        # ใช้ edit history แก้ชื่อ artist/track/album ก่อนที่จะใช้งานต่อ
        edited_artist, edited_track, edited_album, pinned_media = edits_apply(
            info["artist"], info["name"], info["album"]
        )
        self.current_original = {"artist": info["artist"], "track": info["name"], "album": info["album"]}
        self.current_edited = {"artist": edited_artist, "track": edited_track, "album": edited_album}

        track_id = f"{edited_track} - {edited_artist}"
        state = info["state"]
        is_playing = info["playing"]

        is_new = track_id != self.last_track_id
        is_replay = (not is_new) and info["position"] < 3.0 and self.last_position > 5.0

        track_payload = {
            "name": edited_track,
            "artist": edited_artist,
            "album": edited_album,
            "duration": info["duration"],
            "position": info["position"]
        }

        if is_playing and (is_new or is_replay):
            evt = "replay" if is_replay else "play"
            # DB + Last.fm now_playing ยิงทันที — webhook + notification รอ media ก่อน (ได้ artwork)
            db_insert(evt, track_payload)
            threading.Thread(target=lastfm_now_playing, args=(track_payload,), daemon=True).start()
            print(f"[event] {evt}: {track_payload['name']} — {track_payload['artist']}")
            self.last_track_id = track_id
            self.track_start_ts = int(time.time()) - int(info["position"])
            self.has_scrobbled = False
            # เฉพาะ replay เท่านั้นที่อนุญาตให้ scrobble เพลงเดิมได้อีกครั้ง
            # สำหรับเพลงใหม่ (is_new) track_id จะต่างอยู่แล้ว จึงไม่จำเป็นต้องล้าง
            if is_replay:
                self.last_scrobbled_track_id = ""
            self.current_artwork_url = (pinned_media or {}).get("artwork")
            self.current_media = pinned_media or {}
            threading.Thread(
                target=self._prefetch_and_fire_webhook,
                args=(evt, edited_artist, edited_track, edited_album, track_payload, track_id, pinned_media),
                daemon=True
            ).start()
        elif is_playing and self.last_state == "paused":
            self._fire("resume", track_payload)
        elif (not is_playing) and self.last_state == "playing":
            self._fire("pause", track_payload)

        # กันซ้ำ: เพลงที่เคย scrobble แล้วจะไม่ส่งอีก จนกว่าจะ replay (เริ่มใหม่จากต้น) หรือเปลี่ยนเพลง
        if (is_playing and not self.has_scrobbled
                and track_id != self.last_scrobbled_track_id
                and info["duration"] > 30):
            s = load_settings()["scrobble"]
            pct = (info["position"] / info["duration"]) * 100 if info["duration"] > 0 else 0
            if pct >= s["percent"] or info["position"] >= 240:
                if info["position"] >= s["min_seconds"]:
                    self.has_scrobbled = True
                    self.last_scrobbled_track_id = track_id
                    self._fire("scrobble", track_payload)
                    threading.Thread(target=lastfm_scrobble, args=(track_payload, self.track_start_ts), daemon=True).start()
                    threading.Thread(target=_notify_event, args=("scrobble", track_payload, self.current_artwork_url or ""), daemon=True).start()

        self.last_state = state
        self.last_position = info["position"]

        payload = {
            "playing": is_playing,
            "name": edited_track,
            "artist": edited_artist,
            "album": edited_album,
            "original_name": info["name"],
            "original_artist": info["artist"],
            "original_album": info["album"],
            "duration": info["duration"],
            "position": info["position"],
            "artwork_url": self.current_artwork_url,
            "animation_url": (self.current_media or {}).get("animation"),
            "has_scrobbled": self.has_scrobbled,
            "scrobble_percent": (info["position"] / info["duration"] * 100) if info["duration"] > 0 else 0
        }
        
        # Webhook heartbeat logic
        s_webhook = load_settings()["webhook"]
        hb = s_webhook.get("heartbeat_seconds", 0)
        if is_playing and s_webhook["enabled"] and hb > 0:
            if time.time() - get_last_webhook_ts() >= hb:
                self._fire("nowplaying", track_payload, record_db=False)

        self.last_good_payload = payload
        self._write_now_playing(payload)

    def _fire(self, event_type, track, record_db=True):
        """ยิง event สำหรับ resume/pause/scrobble — ใช้ media ที่ prefetch ไว้แล้ว"""
        if record_db:
            db_insert(event_type, track)
        media = dict(self.current_media) if self.current_media else {}
        threading.Thread(target=send_webhook, args=(event_type, track, media), daemon=True).start()
        print(f"[event] {event_type}: {track['name']} — {track['artist']}")

    def _prefetch_and_fire_webhook(self, event_type, artist, track, album, track_payload, track_id, pinned_media=None):
        """ดึง media URLs → อัปเดต state → ยิง webhook (เลียน Music-Scrobbler)"""
        if pinned_media:
            media = pinned_media
        else:
            media = fetch_media_urls(artist, track, album) or {}
        
        # เพลงเปลี่ยนระหว่างรอ network → ทิ้ง
        if track_id != self.last_track_id:
            return
        self.current_media = media
        self.current_artwork_url = media.get("artwork")
        self._update_now_playing_artwork()
        send_webhook(event_type, track_payload, media)
        # ยิง notification หลังได้ artwork แล้ว — ให้รูปโชว์ใน notification ได้
        if event_type in ("play", "replay"):
            threading.Thread(
                target=_notify_event,
                args=(event_type, track_payload, self.current_artwork_url or ""),
                daemon=True
            ).start()

    def _update_now_playing_artwork(self):
        """อัปเดต artwork_url ใน snapshot ปัจจุบันให้ UI เห็นภาพใหม่โดยไม่ต้องรอ tick ถัดไป"""
        cur = dict(CURRENT_NOW_PLAYING.get("data") or {})
        if not cur.get("playing") and not cur.get("name"):
            return
        cur["artwork_url"] = self.current_artwork_url
        self._write_now_playing(cur)

    def reload_media(self):
        """invalidate cache แล้วดึง media URLs ใหม่สำหรับเพลงที่กำลังเล่น"""
        # 1. Re-apply edits ทันที เผื่อ user เพิ่งบันทึกไป
        artist, track, album, pinned_media = edits_apply(
            self.current_original["artist"], 
            self.current_original["track"], 
            self.current_original.get("album", "")
        )
        
        # 2. Update internal state เพื่อให้ tick ถัดไปไม่มองว่าเป็นเพลงใหม่ (is_new)
        self.current_edited = {"artist": artist, "track": track, "album": album}
        self.last_track_id = f"{track} - {artist}"
        
        # 3. ล้าง cache artwork เดิม
        _artwork_cache_invalidate(artist, track, album)
        
        track_payload = {
            "name": track,
            "artist": artist,
            "album": album,
            "duration": 0,
            "position": 0,
        }
        
        # 4. Prefetch media และยิง webhook/nowplaying ใหม่
        threading.Thread(
            target=self._prefetch_and_fire_webhook,
            args=("play", artist, track, album,
                  track_payload, self.last_track_id, pinned_media),
            daemon=True
        ).start()
        return True

    def _write_now_playing(self, data):
        # เก็บใน memory ให้ API อ่านได้ทันที (ไม่ race กับการเขียนไฟล์)
        CURRENT_NOW_PLAYING["data"] = data
        # เขียนไฟล์แบบ atomic: เขียน tmp แล้ว rename (os.replace เป็น atomic บน POSIX)
        try:
            tmp = NOW_PLAYING_PATH.with_suffix(".json.tmp")
            tmp.write_text(json.dumps(data))
            os.replace(tmp, NOW_PLAYING_PATH)
        except Exception:
            pass

