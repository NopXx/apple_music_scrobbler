def _load_bundled_credentials():
    try:
        import _credentials  # type: ignore
        key = (getattr(_credentials, "LASTFM_API_KEY", "") or "").strip()
        sec = (getattr(_credentials, "LASTFM_API_SECRET", "") or "").strip()
        if key or sec:
            return key, sec
    except Exception:
        pass
    env_path = BASE_DIR / ".env"
    if env_path.exists():
        try:
            parsed = {}
            for line in env_path.read_text().splitlines():
                line = line.strip()
                if not line or line.startswith("#") or "=" not in line:
                    continue
                k, v = line.split("=", 1)
                parsed[k.strip()] = v.strip().strip('"').strip("'")
            return parsed.get("LASTFM_API_KEY", "").strip(), parsed.get("LASTFM_API_SECRET", "").strip()
        except Exception:
            pass
    return "", ""

