_settings_lock = threading.Lock()

def _deep_merge(base, override):
    out = dict(base)
    for k, v in override.items():
        if isinstance(v, dict) and isinstance(out.get(k), dict):
            out[k] = _deep_merge(out[k], v)
        else:
            out[k] = v
    return out

def _apply_bundled_lastfm(settings):
    """Overlay bundled LASTFM creds when user hasn't provided their own."""
    if not HAS_BUNDLED_LASTFM:
        return settings
    lf = settings.setdefault("lastfm", {})
    if not lf.get("api_key"):
        lf["api_key"] = BUNDLED_LASTFM_API_KEY
    if not lf.get("api_secret"):
        lf["api_secret"] = BUNDLED_LASTFM_API_SECRET
    return settings

def load_settings():
    with _settings_lock:
        if not SETTINGS_PATH.exists():
            SETTINGS_PATH.write_text(json.dumps(DEFAULT_SETTINGS, indent=2))
            return _apply_bundled_lastfm(json.loads(json.dumps(DEFAULT_SETTINGS)))
        try:
            data = json.loads(SETTINGS_PATH.read_text())
            merged = _deep_merge(DEFAULT_SETTINGS, data)
            # Migration: convert single url to urls list
            wh = merged.get("webhook", {})
            if "url" in wh and not wh.get("urls"):
                old_url = wh.pop("url")
                if old_url:
                    wh["urls"] = [old_url]
            return _apply_bundled_lastfm(merged)
        except Exception:
            return _apply_bundled_lastfm(json.loads(json.dumps(DEFAULT_SETTINGS)))

def save_settings(updates):
    with _settings_lock:
        current = load_settings.__wrapped__() if hasattr(load_settings, "__wrapped__") else None
        if SETTINGS_PATH.exists():
            try:
                current = json.loads(SETTINGS_PATH.read_text())
            except Exception:
                current = {}
        else:
            current = {}
        merged = _deep_merge(_deep_merge(DEFAULT_SETTINGS, current), updates)
        SETTINGS_PATH.write_text(json.dumps(merged, indent=2))
        return merged

