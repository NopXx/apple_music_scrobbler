_edit_lock = threading.Lock()
_edit_cache = {}
_edit_loaded = False

def edits_load():
    global _edit_cache, _edit_loaded
    with _edit_lock:
        if _edit_loaded:
            return _edit_cache
        if EDIT_HISTORY_PATH.exists():
            try:
                txt = EDIT_HISTORY_PATH.read_text("utf-8")
                if txt.strip():
                    data = json.loads(txt)
                    if isinstance(data, dict):
                        _edit_cache = data
                        print(f"[edits] loaded {len(_edit_cache)} items from {EDIT_HISTORY_PATH}")
                    else:
                        print(f"[edits] warning: file {EDIT_HISTORY_PATH} is not a dict")
            except Exception as e:
                print(f"[edits] load error (file exists but failed to read): {e}")
                # DON'T set _edit_loaded = True here if we want to try again,
                # but for safety let's not overwrite an empty cache to disk yet.
                return _edit_cache
        else:
            print(f"[edits] no history file at {EDIT_HISTORY_PATH}, starting fresh")
            _edit_cache = {}
            
        _edit_loaded = True
        return _edit_cache

def edits_save():
    global _edit_loaded
    if not _edit_loaded:
        print("[edits] refuse to save: cache not loaded yet (prevents data loss)")
        return
    with _edit_lock:
        try:
            # Use absolute path to ensure we are writing where we expect
            abs_path = EDIT_HISTORY_PATH.absolute()
            tmp = abs_path.with_suffix(".json.tmp")
            
            data_str = json.dumps(_edit_cache, indent=2, sort_keys=True, ensure_ascii=False)
            tmp.write_text(data_str, encoding="utf-8")
            
            # Verify file was written before replacing
            if tmp.exists() and tmp.stat().st_size > 0:
                os.replace(tmp, abs_path)
            elif not _edit_cache:
                # If cache is empty, we allow writing if it's intentional (e.g. clear)
                # but we should still check if the write succeeded (0 bytes for empty dict is {} -> 2 bytes)
                if tmp.exists() and tmp.stat().st_size >= 2:
                    os.replace(tmp, abs_path)
            else:
                print(f"[edits] save warning: tmp file is empty, aborted replace to {abs_path}")
                
        except Exception as e:
            print(f"[edits] save error: {e}")

def edits_apply(artist, track, album):
    """ถ้าเคยแก้ไว้ → คืนค่าที่แก้แล้ว; ไม่งั้นคืนของเดิม"""
    edits_load()
    key = f"{artist}||||{track}"
    with _edit_lock:
        e = _edit_cache.get(key)
    if not e:
        return artist, track, album, None
    return (
        e.get("artist", artist),
        e.get("track", track),
        e.get("album", album),
        e.get("media")
    )

def edits_upsert(original_artist, original_track, artist, track, album, media=None):
    edits_load()
    key = f"{original_artist}||||{original_track}"
    with _edit_lock:
        _edit_cache[key] = {
            "artist": artist,
            "track": track,
            "album": album,
            "media": media,
            "timestamp": str(time.time())
        }
    edits_save()

def edits_delete(key):
    edits_load()
    with _edit_lock:
        existed = key in _edit_cache
        if existed:
            _edit_cache.pop(key, None)
    if existed:
        edits_save()
    return existed

def edits_replace(new_dict):
    global _edit_cache, _edit_loaded
    with _edit_lock:
        _edit_cache = dict(new_dict)
        _edit_loaded = True
    edits_save()

def edits_list():
    edits_load()
    with _edit_lock:
        items = []
        for key, v in _edit_cache.items():
            parts = key.split("||||", 1)
            orig_artist = parts[0] if parts else ""
            orig_track = parts[1] if len(parts) > 1 else ""
            items.append({
                "key": key,
                "original_artist": orig_artist,
                "original_track": orig_track,
                "artist": v.get("artist", ""),
                "track": v.get("track", ""),
                "album": v.get("album", ""),
                "media": v.get("media"),
                "timestamp": float(v.get("timestamp", 0) or 0)
            })
    items.sort(key=lambda x: x["timestamp"], reverse=True)
    return items

def is_edit_history_format(obj):
    """ตรวจว่า JSON ที่ import มาเป็น edit history format หรือไม่ — ดูจาก key มี `||||` และ value เป็น dict ที่มี track/artist"""
    if not isinstance(obj, dict):
        return False
    if not obj:
        return False
    # อย่าให้ทับซ้อนกับ events export format
    if "events" in obj and isinstance(obj.get("events"), list):
        return False
    for k, v in obj.items():
        if "||||" in k and isinstance(v, dict) and ("track" in v or "artist" in v):
            return True
        break
    return False

