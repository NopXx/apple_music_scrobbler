def _osascript_notify(title: str, subtitle: str, body: str = "", image_url: str = ""):
    # osascript แสดงรูปไม่ได้ — ignore image_url
    try:
        def esc(x):
            return str(x).replace("\\", "\\\\").replace('"', '\\"')
        script = (
            f'display notification "{esc(body)}" '
            f'with title "{esc(title)}" '
            f'subtitle "{esc(subtitle)}"'
        )
        subprocess.Popen(
            ["osascript", "-e", script],
            stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL
        )
    except Exception as e:
        print(f"[notify] osascript error: {e}")

def notify(title: str, subtitle: str = "", body: str = "", image_url: str = ""):
    fn = NOTIFY_CALLBACK.get("fn")
    if fn:
        try:
            # callback ใหม่รับ image_url — ถ้าเป็นเวอร์ชันเก่าจะ TypeError → fallback
            try:
                fn(title, subtitle, body, image_url)
            except TypeError:
                fn(title, subtitle, body)
            return
        except Exception as e:
            print(f"[notify] callback error: {e}")
    _osascript_notify(title, subtitle, body, image_url)

def _notify_event(kind: str, track: dict, artwork_url: str = ""):
    s = load_settings().get("notifications", DEFAULT_SETTINGS["notifications"])
    if not s.get("enabled", True):
        return
    if kind in ("play", "replay") and not s.get("on_play", True):
        return
    if kind == "scrobble" and not s.get("on_scrobble", False):
        return
    name = track.get("name") or "—"
    artist = track.get("artist") or ""
    album = track.get("album") or ""
    if kind == "scrobble":
        notify("✓ Scrobbled", f"{name}", f"{artist} · {album}" if album else artist, artwork_url)
    else:
        label = "▶ Now Playing" if kind == "play" else "↻ Replay"
        notify(label, name, f"{artist} · {album}" if album else artist, artwork_url)

