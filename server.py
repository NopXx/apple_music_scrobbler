_db_lock = threading.Lock()

def db_init():
    with _db_lock:
        conn = sqlite3.connect(DB_PATH)
        conn.execute("""
            CREATE TABLE IF NOT EXISTS events (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                timestamp INTEGER NOT NULL,
                event_type TEXT NOT NULL,
                track_name TEXT NOT NULL,
                artist_name TEXT NOT NULL,
                album_name TEXT,
                duration REAL,
                position REAL
            )
        """)
        conn.execute("CREATE INDEX IF NOT EXISTS idx_events_time ON events(timestamp DESC)")
        conn.execute("CREATE INDEX IF NOT EXISTS idx_events_track ON events(artist_name, track_name)")
        conn.commit()
        conn.close()

def db_insert(event_type, track):
    with _db_lock:
        conn = sqlite3.connect(DB_PATH)
        conn.execute(
            "INSERT INTO events (timestamp, event_type, track_name, artist_name, album_name, duration, position) VALUES (?, ?, ?, ?, ?, ?, ?)",
            (int(time.time()), event_type, track["name"], track["artist"], track.get("album", ""), track.get("duration", 0), track.get("position", 0))
        )
        conn.commit()
        conn.close()

def db_recent(limit=50):
    with _db_lock:
        conn = sqlite3.connect(DB_PATH)
        conn.row_factory = sqlite3.Row
        rows = conn.execute(
            "SELECT * FROM events ORDER BY timestamp DESC LIMIT ?",
            (limit,)
        ).fetchall()
        conn.close()
        return [dict(r) for r in rows]

def db_all():
    with _db_lock:
        conn = sqlite3.connect(DB_PATH)
        conn.row_factory = sqlite3.Row
        rows = conn.execute("SELECT * FROM events ORDER BY timestamp ASC").fetchall()
        conn.close()
        return [dict(r) for r in rows]

def db_bulk_insert(events):
    inserted = 0
    with _db_lock:
        conn = sqlite3.connect(DB_PATH)
        for e in events:
            try:
                conn.execute(
                    "INSERT INTO events (timestamp, event_type, track_name, artist_name, album_name, duration, position) VALUES (?, ?, ?, ?, ?, ?, ?)",
                    (int(e["timestamp"]), e["event_type"], e["track_name"], e["artist_name"], e.get("album_name", "") or "", float(e.get("duration") or 0), float(e.get("position") or 0))
                )
                inserted += 1
            except Exception:
                pass
        conn.commit()
        conn.close()
    return inserted

def db_clear():
    with _db_lock:
        conn = sqlite3.connect(DB_PATH)
        conn.execute("DELETE FROM events")
        conn.commit()
        conn.close()

